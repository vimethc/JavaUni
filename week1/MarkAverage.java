package week1;

public class MarkAverage {
    public static void main(String[] args) {
        double Exam1 = 65;
        double Exam2 = 78;
        double Exam3 = 56;
        
        double average = (Exam1 + Exam2 + Exam3) / 3;
        
        System.out.println("The average of the exam marks is: " + average);
    }
}
